"""
Given a dictionary (tree), that can contains multiple nested structures.
Write a function, that takes element and finds the number of occurrences
of this element in the tree.

Tree can only contains basic structures like:
    str, list, tuple, dict, set, int, bool
>>> exampl_tree = {"first": ["RED", "BLUE"], "second": {"simple_key": ["simple", "RED", "of", "RED", "valued"]}}
>>> print(find_occurrences(exampl_tree, "RED"))
3
"""
from typing import Any


# Example tree:
example_tree = {
    "first": ["RED", "BLUE"],
    "second": {
        "simple_key": ["simple", "list", "of", "RED", "valued"],
    },
    "third": {
        "abc": "BLUE",
        "jhl": "RED",
        "complex_key": {
            "key1": "value1",
            "key2": "RED",
            "key3": ["a", "lot", "of", "values", {"nested_key": "RED"}],
        }
     },
    "fourth": "RED",
}


def find_occurrences(tree: dict, element: Any) -> int:
    # we output the values of the dictionary to a string
    # and count the number of occurrences of "element" in this string
    return str(tree.values()).count(str(element))


